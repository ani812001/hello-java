
public class AssigniningVariables {

    public static void main(String[] args)
    {
        int x,y,z;

        //Assingment variables is done  from right to left.

        x=y=z=2*5+4;

        System.out.println("x:" +x);
        System.out.println("y : " +y);
        System.out.println("z :" +z);
    }
}
